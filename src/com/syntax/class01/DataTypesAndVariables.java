package com.syntax.class01;

public class DataTypesAndVariables {

	public static void main(String[] args) {
		
		// type name = assign value
		//numeric
		
		// type name = assign value
	    byte variable1=4; //-128 to 127
	    
		short variable2=100; //-32768 to 32767
		
		int anyName=1000; //more than 16 numbers digits wont work
		
		long variable3=1000000879876665L;
		
		//to store floating numbers
		
		float var1=12.99f;
		double var2=9.99999;
		
		//represent yes or no (true or false)
		boolean myVariable=true;
		boolean anotherVariable=false;
		
		//to represent single character
		char myBox='C';
		char myBox2='&';
		char myBox3='7';
		
		System.out.println(variable1);
		System.out.println(variable2);
		System.out.println(anyName);
		System.out.println(variable3);
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(myVariable);
		System.out.println(anotherVariable);
		System.out.println(myBox);
		System.out.println(myBox2);
		System.out.println(myBox3);
		
	}

}
