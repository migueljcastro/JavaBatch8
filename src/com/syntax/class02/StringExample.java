package com.syntax.class02;

public class StringExample {

	public static void main(String[] args) {
		
		// String is a non primitive datatype
		String variable="Hello";
		
		System.out.println(variable);
		
		String mySentence="Today is Sunday, September 27";	
		
		System.out.println(mySentence);
		
		//int number="99"; Error for anything we put inside "" --> becomes a string
		
		String number="99";//we will get 99 as a String
		
		//String num=100;
		
		System.out.println(number);
		
		//char c="C"; Error because anything we put inside"" --> becomes String. char my be single '';
		
		String oneLetter="Y";
		System.out.println(oneLetter);
	}

}
